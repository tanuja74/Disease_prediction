import  streamlit as st
import joblib
from streamlit_option_menu import option_menu
Diabetes_model=joblib.load('Diabetes_Project1')
Heart_model=joblib.load('Heart_model')
Parkinsons_model=joblib.load('Parkinson_project')

#creating side bar
with st.sidebar:
    selected=option_menu('Multiple Disease Prediction system',
                         ['Diabetes Prediction',
                           'Heart Prediction',
                           'Parkinsons Prediction'],
                         icons=['activity','heart','person'],
                         default_index=0)

#Diabetes Prediction Page
# Diabetes Prediction Page
if selected == 'Diabetes Prediction':

    # page title
    st.title('Diabetes Prediction using ML')

    # getting the input data from the user
    col1, col2, col3 = st.columns(3)

    with col1:
        Pregnancies = st.text_input('Number of Pregnancies')

    with col2:
        Glucose = st.text_input('Glucose Level')

    with col3:
        BloodPressure = st.text_input('Blood Pressure value')

    with col1:
        SkinThickness = st.text_input('Skin Thickness value')

    with col2:
        Insulin = st.text_input('Insulin Level')

    with col3:
        BMI = st.text_input('BMI value')

    with col1:
        DiabetesPedigreeFunction = st.text_input('Diabetes Pedigree Function value')

    with col2:
        Age = st.text_input('Age of the Person')


    # code for Prediction
    diab_diagnosis = ''

    # creating a button for Prediction

    if st.button('Diabetes Test Result'):

        user_input = [Pregnancies, Glucose, BloodPressure, SkinThickness, Insulin,
                      BMI, DiabetesPedigreeFunction, Age]

        user_input = [float(x) for x in user_input]

        diab_prediction = Diabetes_model.predict([user_input])

        if diab_prediction[0] == 1:
            diab_diagnosis = 'The person is diabetic'
        else:
            diab_diagnosis = 'The person is not diabetic'

    st.success(diab_diagnosis)

if (selected=='Heart Prediction'):

    #page title
        st.title('Heart  Prediction using ML')
        col1,col2,col3,col4=st.columns(4)
        with col1:
         Age=st.text_input("Age")
        with col2:
         Sex=st.text_input('Gender:0=F,1=M')
        with col3:
         ChestPainType=st.text_input('chest pain type')
        with col4:
         RestingBP=st.text_input('Resting Bp value')
        with col1:
         Cholesterol=st.text_input('cholestrol level')
         with col2:
            FastingBS=st.text_input('fasting Blood sugar level')
         with col3:
          RestingECG=st.text_input('Resting ecg type')
         with col4:
          MaxHR=st.text_input('maximum heart rate')
         with col1:
          ExerciseAngina=st.text_input('ExerciseAngina')
         with col2:
          Oldpeak=st.text_input('old peak value')
         with col3:
          ST_Slope=st.text_input('Type of ST_slope')

        hea_diagnosis=''
        #creating button for prediction
        if st.button('Heart test Result'):
            hea_prediction=Heart_model.predict([[Age,Sex,ChestPainType,RestingBP,Cholesterol,FastingBS,RestingECG,MaxHR,ExerciseAngina,Oldpeak,ST_Slope]])
            if (hea_prediction[0]==1):
                hea_diagnosis='The person is having Heart Disease'
            else:
                hea_diagnosis='The person does not have Heart Disease'
        st.success(hea_diagnosis)


if selected == "Parkinsons Prediction":

    # page title
    st.title("Parkinson's Disease Prediction using ML")

    col1, col2, col3, col4, col5 = st.columns(5)

    with col1:
        fo = st.text_input('MDVP:Fo(Hz)')

    with col2:
        fhi = st.text_input('MDVP:Fhi(Hz)')

    with col3:
        flo = st.text_input('MDVP:Flo(Hz)')

    with col4:
        Jitter_percent = st.text_input('MDVP:Jitter(%)')

    with col5:
        Jitter_Abs = st.text_input('MDVP:Jitter(Abs)')

    with col1:
        RAP = st.text_input('MDVP:RAP')

    with col2:
        PPQ = st.text_input('MDVP:PPQ')

    with col3:
        DDP = st.text_input('Jitter:DDP')

    with col4:
        Shimmer = st.text_input('MDVP:Shimmer')

    with col5:
        Shimmer_dB = st.text_input('MDVP:Shimmer(dB)')

    with col1:
        APQ3 = st.text_input('Shimmer:APQ3')

    with col2:
        APQ5 = st.text_input('Shimmer:APQ5')

    with col3:
        APQ = st.text_input('MDVP:APQ')

    with col4:
        DDA = st.text_input('Shimmer:DDA')

    with col5:
        NHR = st.text_input('NHR')

    with col1:
        HNR = st.text_input('HNR')

    with col2:
        RPDE = st.text_input('RPDE')

    with col3:
        DFA = st.text_input('DFA')

    with col4:
        spread1 = st.text_input('spread1')

    with col5:
        spread2 = st.text_input('spread2')

    with col1:
        D2 = st.text_input('D2')

    with col2:
        PPE = st.text_input('PPE')

    # code for Prediction
    parkinsons_diagnosis = ''

    # creating a button for Prediction
    if st.button("Parkinson's Test Result"):

        user_input = [fo, fhi, flo, Jitter_percent, Jitter_Abs,
                      RAP, PPQ, DDP,Shimmer, Shimmer_dB, APQ3, APQ5,
                      APQ, DDA, NHR, HNR, RPDE, DFA, spread1, spread2, D2, PPE]

        user_input = [float(x) for x in user_input]

        parkinsons_prediction = Parkinsons_model.predict([user_input])

        if parkinsons_prediction[0] == 1:
            parkinsons_diagnosis = "The person has Parkinson's disease"
        else:
            parkinsons_diagnosis = "The person does not have Parkinson's disease"

    st.success(parkinsons_diagnosis)